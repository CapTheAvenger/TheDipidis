/* ── state ───────────────────────────────────────────────────── */
let currentLang = localStorage.getItem(I18N_STORAGE_KEY)
                  || I18N_DEFAULT_LANG;
if (!I18N_SUPPORTED.includes(currentLang)) currentLang = I18N_DEFAULT_LANG;

/* ── core API ────────────────────────────────────────────────── */

/**
 * Return the translated string for `key` in the current language.
 * Falls back to English, then returns the key itself if nothing found.
 */
function t(key) {
  const lang = translations[currentLang];
  if (lang && lang[key] !== undefined) return lang[key];
  const fallback = translations[I18N_DEFAULT_LANG];
  if (fallback && fallback[key] !== undefined) return fallback[key];
  return key;                     // last resort: show the key
}

/**
 * Switch to `lang` ('en' or 'de'), persist choice, update DOM.
 * Dispatches a 'languageChanged' CustomEvent on document so other
 * modules (charts, dynamic tables) can re-render.
 */
function switchLanguage(lang) {
  if (!I18N_SUPPORTED.includes(lang)) return;
  currentLang = lang;
  localStorage.setItem(I18N_STORAGE_KEY, lang);
  document.documentElement.lang = lang;
  updateTranslationsInDOM();
  document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

/**
 * Return the currently active language code ('en' | 'de').
 */
function getLang() {
  return currentLang;
}

/* ── DOM updater ─────────────────────────────────────────────── */

/**
 * Walk all elements with `data-i18n` and replace visible text.
 *
 * Supported attributes:
 *   data-i18n="key"              → sets innerHTML
 *   data-i18n-placeholder="key"  → sets placeholder
 *   data-i18n-title="key"        → sets title attribute
 *   data-i18n-aria="key"         → sets aria-label
 */
function updateTranslationsInDOM() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (!key) return;
    const translated = t(key);
    const childElements = Array.from(el.children);
    if (childElements.length > 0) {
      // If the element has a dedicated label child, update that instead
      const labelChild = el.querySelector('.menu-item-label');
      if (labelChild) {
        labelChild.textContent = translated;
        return;
      }
      // Preserve other child elements (e.g. info-icon spans) — only replace direct text
      const textNodes = Array.from(el.childNodes).filter(n => n.nodeType === Node.TEXT_NODE && n.textContent.trim());
      if (textNodes.length > 0) {
        textNodes[0].textContent = translated + ' ';
      }
      // If no text nodes exist, don't insert one (the element uses child elements for text)
    } else {
      el.innerHTML = translated;
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (key) el.placeholder = t(key);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    if (key) el.title = t(key);
  });
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria');
    if (key) el.setAttribute('aria-label', t(key));
  });

  // Update the language toggle button label (show active language)
  const toggle = document.getElementById('langToggleBtn');
  if (toggle) toggle.textContent = currentLang === 'de' ? 'DE' : 'EN';

  // Update CSS custom property for close-button tooltips
  document.documentElement.style.setProperty('--close-tooltip', `'${t('btn.close')}'`);
}

// Expose i18n functions globally
window.t = t;
window.switchLanguage = switchLanguage;
window.getLang = getLang;
window.updateTranslationsInDOM = updateTranslationsInDOM;

/* ── auto-init on load ───────────────────────────────────────── */
document.addEventListener('DOMContentLoaded', () => {
  document.documentElement.lang = currentLang;
  updateTranslationsInDOM();
});
